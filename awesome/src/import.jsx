function add(x, y) {
    const add = x + y
    return add
}

function sub(x, y) {
    const sub = x - y
    return sub
}

function div(x, y) {
    const div = x / y
    return div.toFixed(2)
}

function mult(x, y) {
    const mult = x * y
    return mult
}

